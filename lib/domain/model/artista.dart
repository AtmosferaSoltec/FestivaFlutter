class Artista {
  final int id;
  final String nombre;
  final String tipo;
  final String descrip;
  final String biografia;
  final String tags;
  final String urlFoto;
  final String urlFoto2;

  Artista({
    required this.id,
    required this.nombre,
    required this.tipo,
    required this.descrip,
    required this.biografia,
    required this.tags,
    required this.urlFoto,
    required this.urlFoto2,
  });
}
